            <h2>Proses Produksi</h2>
            <img src="assets/image/produksi.jpg" alt="" class="gambar">
            <p>Adapun tahap-tahap produksi yang kami lakukan dalam usaha bisnis bibit kami, tahap-tahap tersebut meliputi sebagai berikut :</p>
            
            <h4>1. Tahap Pemilihan Biji</h4>
            <p class="hempat">Kami melakukan pemilihan biji dari buah yang sudah matang berkualitas, dan kami memilih biji berkualitas baik yang akan dijadikannya
            sebagai calon benih bibit unggul. Dalam proses ini kami melakukan langkah penyortiran biji yang layak pakai dan membuang biji yang tidak
            layak pakai. Biji yang layak pakai memiliki ciri ciri yakni : <br>
            a. Biji memiliki bentuk yang sempurna,<br>
            b. Biji tidak lembek,<br>
            c. Biji tidak lembab,<br>
            d. Biji tidak berubah warna dari warna asalnya,<br>
            e. Biji memiliki ukuran pada umumnya,<br>
            f. Biji tidak busuk. </p>

            <h4>2. Tahap Penanaman Biji</h4>
            <p class="hempat">
                Pada tahap ini biji yang telah dipilih akan ditanam pada polybag ukuran kecil hingga menjadi benih.
            </p>

            <h4>3. Tahap Pemilihan Benih</h4>
            <p class="hempat">
                Setelah biji berubah menjadi benih, maka dilakukannya pemilihan benih. Pemilihan benih dilakukan agar bibit yang akan dijual dan ditanam konsumen dapat
                 berkembang dan berbuah dengan baik dan bermanfaat. Benih yang baik memiliki kriteria, daun yang sempurna, tinggi yang sempurna dalam kurun waktu tertentu,
                  warna yang sempurna seperti pada umumnya.
            </p>

            <h4>4. Tahap Perawatan Bibit</h4>
            <p class="hempat">
                Setelah benih melewati tahap pemilihan maka benih akan dirawat dengan baik agar menjadi bibit yang unggul. Perawatan bibit ini dapat dilakukan dengan cara sebagai berikut :
                <br>
                1. Melakukan penyiraman rutin pada bibit,<br>
                2. Memperhatikan kadar air dan ph tanah pada media tanam bibit,<br>
                3. Melakukan penyemprotan obat jika bibit terserang jamur dll,<br>
                4. Memindah bibit ke media tanam yang baru jika telah melewati masa pindah media tanam,<br>
                5. Melakukan langkah 1-4 dengan rutin.
            </p>

